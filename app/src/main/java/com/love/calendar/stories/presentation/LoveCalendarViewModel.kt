    init {
        createNotificationChannel()
        loadStories()
    }
    
    private fun loadStories() = viewModelScope.launch {
        _isLoading.value = true
        val count = repository.getStoryCount()
        if (count == 0) {
            repository.insertAllStories(allStoriesList)
        }
        _allStories.value = repository.getAllStories()
        _currentDay.value = repository.getCurrentDay()
        _isLoading.value = false
    }
    
    fun loadStory(day: Int) = viewModelScope.launch {
        _currentStory.value = repository.getStory(day)
        repository.getStoryProgress(day).collect { progress ->
            _currentProgress.value = progress
        }
    }
    
    fun updateProgress(sentenceIndex: Int, isCompleted: Boolean) = viewModelScope.launch {
        _currentStory.value?.let { story ->
            repository.updateProgress(story.day, sentenceIndex, isCompleted)
            _currentProgress.value = _currentProgress.value?.copy(
                lastReadSentence = sentenceIndex,
                isCompleted = isCompleted,
                lastReadDate = System.currentTimeMillis()
            )
        }
    }
    
    fun resetProgress() = viewModelScope.launch {
        _currentStory.value?.let { story ->
            repository.updateProgress(story.day, 0, false)
            _currentProgress.value = _currentProgress.value?.copy(
                lastReadSentence = 0,
                isCompleted = false
            )
        }
    }
    
    fun startStory(onSentenceShown: (Int) -> Unit) = viewModelScope.launch {
        _isPlaying.value = true
        val startIndex = _currentProgress.value?.lastReadSentence ?: 0
        val totalSentences = _currentStory.value?.sentences?.size ?: 0
        
        for (i in startIndex until totalSentences) {
            if (!_isPlaying.value) break
            updateProgress(i, i == totalSentences - 1)
            onSentenceShown(i)
            delay(4800)
        }
        _isPlaying.value = false
    }
    
    fun stopStory() {
        _isPlaying.value = false
        storyJob?.cancel()
    }
    
    fun toggleMusic() {
        if (isMusicPlaying.value) {
            mediaPlayer?.pause()
            isMusicPlaying.value = false
        } else {
            if (mediaPlayer == null) {
                createMusicPlayer()
            }
            mediaPlayer?.start()
            isMusicPlaying.value = true
        }
    }
    
    private fun createMusicPlayer() {
        try {
            // Simple tone generator using AudioTrack
            // For real music, add an MP3 to res/raw/
            mediaPlayer = MediaPlayer.create(context, generateDummySound())
            mediaPlayer?.isLooping = true
            mediaPlayer?.setVolume(0.5f, 0.5f)
        } catch (e: Exception) {
            e.printStackTrace()
        }
    }
    
    private fun generateDummySound(): Int {
        // This returns a placeholder - add actual music to res/raw/
        return 0
    }
    
    fun showSurprise() {
        showSurpriseDialog.value = true
    }
    
    suspend fun resetToToday() {
        repository.resetToToday()
        _currentDay.value = repository.getCurrentDay()
    }
    
    private fun createNotificationChannel() {
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.O) {
            val channel = NotificationChannel(
                "love_stories_channel",
                "قصص الحب",
                NotificationManager.IMPORTANCE_HIGH
            ).apply {
                description = "إشعارات يومية بقصة جديدة من تقويم الحب"
                setShowBadge(true)
            }
            val manager = context.getSystemService(NotificationManager::class.java)
            manager.createNotificationChannel(channel)
        }
    }
